						<h3 class="my-3">Summary</h3>
			<p>Mean annual grain and straw yields for each treatment strip of the Broadbalk Wheat Experiment, 1852-1925. Also other agronomic information, including sowing and harvest dates, amount, type and date of application of treatments, and winter wheat cultivars. </p>			<h3>Methods</h3>
			<p>The treatment strips (plots) extend the whole length of the field, each testing a different fertilizer or manure treatment. In some years, the strips were divided into two or more sub-plots. Annual yield data presented here for each strip is the mean of all sub-plots, except when the fertilizer treatments were different (plots 9a and 9b, 15a and 15b, selected years), or if a sub-plot received a different treatment for one year, when it was excluded from the mean. See 'Broadbalk plan and cropping 1852-1925' for full details. Yield data from 1844-1851 is not included, as the fertilizer treatments were not fully established until 1852. </p>			<h3>Technical Information</h3>
			<p>Wheat grain and straw yields have been recorded every year since the experiment began, with the first harvest in 1844 (the crop was sown in autumn 1843). Dried grain and straw samples have also been kept for chemical analysis since 1844; these are preserved in the Rothamsted Sample Archive. Yields are expressed at field moisture content,
approximately 85% dry matter. </p>
